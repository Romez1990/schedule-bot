class BotTextMessages:
    def message_text_start(self) -> str:
        return '''
        –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤—É—é –¥–æ—Ä–æ–≥–æ–π —Å—Ç—É–¥–µ–Ω—Çüëã–∏–ª–∏ —É–∂–µ –Ω–µ —Å—Ç—É–¥–µ–Ω—Ç\n
        ü¶æ–Ø –±–æ—Ç –ø–æ–∑–≤–æ–ª—è—é—â–µ–µ –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–∏—Ç—å —Ç–µ–±–µ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ —Ç–≤–æ–µ–π –≥—Ä—É–ø–ø—ãüë®‚Äçüíª\n
        –ù–∞–ø–∏—à–∏ –º–Ω–µ: /–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã] (–±–µ–∑ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–π —Å–∫–æ–±–∫–∏)
                            '''

    def message_text_help(self) -> str:
        return '''
       /start - –∫–æ–º–∞–Ω–¥–∞ –¥–ª—è —Å—Ç–∞—Ä—Ç–∞ –±–æ—Ç–∞ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –≤–∫–ª—é—á–µ–Ω)\n
       /–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã] - –ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è –Ω–∞ —Ä–∞—Å—Å—ã–ª–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ\n
       /–æ—Ç–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã] - –æ—Ç–ø–∏—Å–∞—Ç—å—Å—è –æ—Ç —Ä–∞—Å—Å—ã–ª–∫–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ\n
       /—Ç–µ–º–∞ [—Ç—ë–º–Ω–∞—è/—Å–≤–µ—Ç–ª–∞—è] - –ø–æ–ª—É—á–∞—Ç—å—Å—è —Ä–∞—Å—Å—ã–ª–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ –≤ —Ç—ë–º–Ω–æ–π –∏–ª–∏ —Å–≤–µ—Ç–ª–æ–π —Ç–µ–º–µ\n
                   '''

    def message_subscribe(self, username_group: str) -> str:
        return self.__messages_subscribe(username_group, True)

    def message_unsubscribe(self, username_group: str) -> str:
        return self.__messages_subscribe(username_group, False)

    def __messages_subscribe(self, username_group: str, sub_or_unsub: bool) -> str:
        subscribe = '–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è' if sub_or_unsub else '–æ—Ç–ø–∏—Å–∞—Ç—å—Å—è'
        return f'–í—ã —É—Å–ø–µ—à–Ω–æ {subscribe} –Ω–∞ —Ä–∞—Å—Å—ã–ª–∫—É –≥—Ä—É–ø–ø—ã {username_group}'

    def message_theme(self, theme: str) -> str:
        return f'–í—ã —É—Å–ø–µ—à–Ω–æ –≤—ã–±—Ä–∞–ª–∏ —Ä–∞—Å—Å—ã–ª–∫—É —Ç–µ–º—ã: {theme}'
