    def message_text_help(self, platform: str) -> str:
        if platform == 'telegram':
            return '''
        <em>/start</em> - <strong>–∫–æ–º–∞–Ω–¥–∞ –¥–ª—è —Å—Ç–∞—Ä—Ç–∞ –±–æ—Ç–∞</strong> (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –≤–∫–ª—é—á–µ–Ω)\n
        <em>/–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã]</em> - <strong>–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è –Ω–∞ —Ä–∞—Å—Å—ã–ª–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ</strong>\n
        <em>/–æ—Ç–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã]</em> - <strong>–æ—Ç–ø–∏—Å–∞—Ç—å—Å—è –æ—Ç —Ä–∞—Å—Å—ã–ª–∫–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ</strong>\n
        <em>/—Ç–µ–º–∞ [—Ç—ë–º–Ω–∞—è/—Å–≤–µ—Ç–ª–∞—è]</em> - <strong>–ø–æ–ª—É—á–∞—Ç—å—Å—è —Ä–∞—Å—Å—ã–ª–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ –≤ —Ç—ë–º–Ω–æ–π –∏–ª–∏ —Å–≤–µ—Ç–ª–æ–π —Ç–µ–º–µ</strong>\n
                '''
        elif platform == 'vk':
            return '''
                   /start - <strong>–∫–æ–º–∞–Ω–¥–∞ –¥–ª—è —Å—Ç–∞—Ä—Ç–∞ –±–æ—Ç–∞ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –≤–∫–ª—é—á–µ–Ω)\n
                   /–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã] - –ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è –Ω–∞ —Ä–∞—Å—Å—ã–ª–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ\n
                   /–æ—Ç–ø–∏—Å–∞—Ç—å—Å—è [–ù–∞–∑–≤–∞–Ω–∏–µ_–ì—Ä—É–ø–ø—ã] - –æ—Ç–ø–∏—Å–∞—Ç—å—Å—è –æ—Ç —Ä–∞—Å—Å—ã–ª–∫–∏ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ\n
                   /—Ç–µ–º–∞ [—Ç—ë–º–Ω–∞—è/—Å–≤–µ—Ç–ª–∞—è] - –ø–æ–ª—É—á–∞—Ç—å—Å—è —Ä–∞—Å—Å—ã–ª–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ –≤ —Ç—ë–º–Ω–æ–π –∏–ª–∏ —Å–≤–µ—Ç–ª–æ–π —Ç–µ–º–µ\n
                   '''
        else:
            return 'Error'

    def message_subscribe(self, username_group: str) -> str:
        return self.__messages_subscribe(username_group, True)

    def message_unsubscribe(self, username_group: str) -> str:
        return self.__messages_subscribe(username_group, False)

    def __messages_subscribe(self, username_group: str, sub_or_unsub: bool) -> str:
        subscribe = '–ø–æ–¥–ø–∏—Å–∞—Ç—å—Å—è' if sub_or_unsub else '–æ—Ç–ø–∏—Å–∞—Ç—å—Å—è'
        return f'<strong>–í—ã —É—Å–ø–µ—à–Ω–æ {subscribe} –Ω–∞ —Ä–∞—Å—Å—ã–ª–∫—É –≥—Ä—É–ø–ø—ã <em>{username_group}' \
               f'</em></strong>'

    def message_theme(self, theme: str) -> str:
        return f'<strong>–í—ã —É—Å–ø–µ—à–Ω–æ –≤—ã–±—Ä–∞–ª–∏ —Ä–∞—Å—Å—ã–ª–∫—É —Ç–µ–º—ã: <em>{theme}</em></strong>'
